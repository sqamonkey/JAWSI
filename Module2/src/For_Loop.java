public class For_Loop {
    public static void main(String[] args){

        System.out.println("forLoop 1");
        for (int i=0; i<10; i++){
            System.out.println(i);
        }

        System.out.println("\nforLoop 2");
        int sum = 0;
        for (int n=0; n<=100; n++){
            System.out.println(sum = sum+n);
        }
    }
}